SELECT м_нжемйх.йнд, м_беднлнярх.хд
FROM м_нжемйх
         INNER JOIN м_беднлнярх ON м_беднлнярх.нжемйю = м_нжемйх.йнд
WHERE м_нжемйх.йнд = 'МЕЪБЙЮ'
  AND м_беднлнярх.вкбй_хд = 153285
  AND м_беднлнярх.вкбй_хд < 142390;


SELECT м_кчдх.хд, м_насвемхъ.вкбй_хд, м_свемхйх.хд
FROM м_кчдх
         LEFT JOIN м_насвемхъ ON м_кчдх.хд = м_насвемхъ.вкбй_хд
         LEFT JOIN м_свемхйх ON м_свемхйх.вкбй_хд = м_насвемхъ.вкбй_хд
WHERE м_кчдх.хлъ > 'юКЕЙЯЮМДП'
  AND м_насвемхъ.мгй::integer > 001000;